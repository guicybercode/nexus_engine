defmodule ProjectElixirRustLuaWeb do
  def static_paths, do: ~w(assets fonts images js css favicon.ico robots.txt)

  def controller do
    quote do
      use Phoenix.Controller,
        namespace: ProjectElixirRustLuaWeb,
        formats: [:html, :json],
        layouts: [html: ProjectElixirRustLuaWeb.Layouts]

      import Plug.Conn
      use Gettext, backend: ProjectElixirRustLuaWeb.Gettext

      unquote(verified_routes())
    end
  end

  def router do
    quote do
      use Phoenix.Router, helpers: false
      import Plug.Conn
      import Phoenix.Controller
      import Phoenix.LiveView.Router
    end
  end

  def channel do
    quote do
      use Phoenix.Channel
      use Gettext, backend: ProjectElixirRustLuaWeb.Gettext
    end
  end

  def html do
    quote do
      use Phoenix.Component

      import Phoenix.Controller,
        only: [get_csrf_token: 0, view_module: 1, view_template: 1]

      unquote(html_helpers())
    end
  end

  defp html_helpers do
    quote do
      import Phoenix.HTML
      import ProjectElixirRustLuaWeb.CoreComponents
      use Gettext, backend: ProjectElixirRustLuaWeb.Gettext

      unquote(verified_routes())
    end
  end

  def verified_routes do
    quote do
      use Phoenix.VerifiedRoutes,
        endpoint: ProjectElixirRustLuaWeb.Endpoint,
        router: ProjectElixirRustLuaWeb.Router,
        statics: ProjectElixirRustLuaWeb.static_paths()
    end
  end

  defmacro __using__(which) when is_atom(which) do
    apply(__MODULE__, which, [])
  end
end
